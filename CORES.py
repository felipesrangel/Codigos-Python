'''CÓDIGO ANSI
#Primeiro começar com \, depois [ e preenche com o style, text e back (opcionais) e por fim letra m
Código de STYLE:
0 = none
1 = bold
4 = underline
7 = Negative (inversão)'''

'''Código de BACK / TEXT    :
40 / 30 = Branco  
41 / 31 = Vermelho 
42 / 32 = Verde
43 / 33 = Amarelo 
44 / 34 = Azul
45 / 35 = Magenta
46 / 36 = Ciano
47 / 37 = Cinza'''


'''EXEMPLOS
\033[0;30;41m
\033[4;33;44m
\033[1;35;43m
\033[30;42m
\033[m
\033[7;30m'''